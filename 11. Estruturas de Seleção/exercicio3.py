# 3) Obter a cotação atual do dólar americado e do euro e exibí-las com precisão de 2 casas decimais.

# Entrada de dados
valor_dolar = float(input("Digite a cotação atual do Dólar: "))
valor_euro = float(input("Digite a cotação atual do Euro: "))

# Exibindo as cotações com 2 casas decimais
print(f"Cotação do Dólar: {valor_dolar:.2f}")
print(f"Cotação do Euro: {valor_euro:.2f}")


